export const getPriceData = () => {
  const arr = [];

  for (let i = 0; i < 180; i++) {
    arr.push(
      `${Math.floor(
        Math.random() * 10 * (Math.random() * 10),
      )}만원`,
    );
  }
  return arr;
};

